// while loop

let i = 0;
while(i <= 10){
    console.log(`Value of i is ${i}`);
    i = i + 2;    
}

const myArray = ["Ironman", "Hulk" , "Captain America", "Thor"];

let j = 0;
while(j < myArray.length){
    console.log(myArray[j]);
    j++;
}


// do-while loop

let index = 0;
do{
    console.log(index);
    index++;
}while(index <= 10);